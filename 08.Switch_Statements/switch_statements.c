#include<stdio.h>


int main(){


	char grade;


	printf("Enter a letter grade: ");
	scanf("%c", &grade);


	switch(grade){
		case 'A':
			printf("Perfect!\n");
			break;

		case 'B':
			printf("you did Good!\n");
			break;

		case 'C':
			printf("you did okay!\n");
			break;

		case 'D':
			printf("At least it is not an F!\n");
			break;

		case 'F':
			printf("You FAIEd!\n");
			break;
		default:
			printf("please enter only valid grades\n");
	}


	return 0;



}
